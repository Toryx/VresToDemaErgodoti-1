package com.toryx.vrestodemaergodoti;

public class Code {
    private String code;
    private String apostoleas;
    private String paraliptis;
    private String cond;

    public Code(){

    }

    public String getCond() {
        return cond;
    }

    public void setCond(String cond) {
        this.cond = cond;
    }

    public String getParaliptis() {
        return paraliptis;
    }

    public void setParaliptis(String paraliptis) {
        this.paraliptis = paraliptis;
    }

    public String getApostoleas() {
        return apostoleas;
    }

    public void setApostoleas(String apostoleas) {
        this.apostoleas = apostoleas;
    }


    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
